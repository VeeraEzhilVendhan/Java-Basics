package com.veera.java5.operators;

public class UnaryOperators {
	
	public static void main(String[] args) {
		
		int a=5;
		System.out.println("Value of a"); //5
		System.out.println("pre increment of a - "+ ++a);//6
		System.out.println("post increment of a - "+ a++);//6
		System.out.println("pre decrement of a - "+ --a);//6
		System.out.println("post decrement of a - "+ a--);//6
		System.out.println("denoting sign unary plus "+ +a);//5
		System.out.println("denoting sign unary minus "+ -a);//-5
		
		
	}

}
