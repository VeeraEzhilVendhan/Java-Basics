package com.veera.java5.operators;

public class LogicalOperator {
	
	public static void main(String[] args) {

		System.out.println(true && 5<8);
		System.out.println(5<8 && false);
		System.out.println(true || 5>8);
		System.out.println(5>8 || false);
		System.out.println(!(5>8));
		System.out.println(!true);
	
	}

}
